use parse_display::Display;

#[derive(Display, Debug, Clone, Copy, PartialEq, Eq)]
pub enum BinOperator {
    #[display("+")]
    Add,
    #[display("&&")]
    And,
    #[display("=")]
    Assign,
    #[display("+=")]
    AssignAdd,
    #[display("&=")]
    AssignBitwiseAnd,
    #[display("|=")]
    AssignBitwiseOr,
    #[display("/=")]
    AssignDivide,
    #[display("<<=")]
    AssignLShift,
    #[display("%=")]
    AssignModulo,
    #[display("*=")]
    AssignMultiply,
    #[display("**=")]
    AssignPow,
    #[display(">>=")]
    AssignRShift,
    #[display("-=")]
    AssignSubtract,
    #[display("+^=")]
    AssignXor,
    At,
    #[display("&")]
    BitwiseAnd,
    #[display("|")]
    BitwiseOr,
    #[display("/")]
    Divide,
    #[display("==")]
    Equal,
    #[display("?")]
    Filter,
    FunctionCall,
    #[display(">")]
    Greater,
    #[display(">=")]
    GreaterOrEqual,
    #[display("<<")]
    LShift,
    #[display("<")]
    Lower,
    #[display("<=")]
    LowerOrEqual,
    #[display("@")]
    Map,
    #[display("%")]
    Modulo,
    #[display("*")]
    Multiply,
    #[display("!=")]
    NotEqual,
    #[display("||")]
    Or,
    #[display("**")]
    Pow,
    #[display(">>")]
    RShift,
    #[display("-")]
    Subtract,
    #[display("^")]
    Xor,
}
